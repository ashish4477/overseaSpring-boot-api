package com.bearcode.ovf.tools.candidate.model;

/**
 * Enumeration of the types of office available.
 * @author IanBrown
 *
 * @since Jun 14, 2012
 * @version Jun 15, 2012
 */
public enum OfficeType {

	/**
	 * the commissioner for a county.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	COUNTY_COMMISSIONER,
	
	/**
	 * the president.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	PRESIDENT,
	
	/**
	 * a state representative.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	STATE_REPRESENTATIVE, 
	
	/**
	 * a state senator.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	STATE_SENATOR, 
	
	/**
	 * a U.S. representative.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	US_REPRESENTATIVE,
	
	/**
	 * a U.S. senator.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	US_SENATOR, 
	
	/**
	 * a supervisor for soil and water.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	SOIL_AND_WATER_SUPERVISOR, 
	
	/**
	 * chief justice for the state supreme court.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	SUPREME_COURT_CHIEF_JUSTICE, 
	
	/**
	 * an associate justice for the state supreme court.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	SUPREME_COURT_ASSOCIATE_JUSTICE, 
	
	/**
	 * a judge for the state court of appeals.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	COURT_OF_APPEALS_JUDGE, 
	
	/**
	 * a judge for a district court.
	 * @author IanBrown
	 * @since Jun 14, 2012
	 * @version Jun 14, 2012
	 */
	DISTRICT_COURT_JUDGE,
	
	/**
	 * a council member.
	 * @author IanBrown
	 * @since Jun 15, 2012
	 * @version Jun 15, 2012
	 */
	COUNCIL_MEMBER, 
	
	/**
	 * a mayor.
	 * @author IanBrown
	 * @since Jun 15, 2012
	 * @version Jun 15, 2012
	 */
	MAYOR;
}
